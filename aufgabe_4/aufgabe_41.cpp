#include <iostream>
using namespace std;

int main() {
    //Einfache Ausgabe Text
    cout << "Hellom, World!" << endl;

    //Einfache Ausgabe Integer
    int j = 1;
    cout << j << endl;

    //Einfache Ausgabe float
    float f = 4.56;
    cout << f << endl;

    //Ausgabe einer Konstanten
    const double PI = 3.1415;
    cout << PI << endl;

    //Ausgabe
    cout << "Integer Variable j = " << j << endl;
    cout << "Float Variable f   = " << f << endl;
    cout << "Konstante          Pi = " << PI << endl;

    return 0;
}